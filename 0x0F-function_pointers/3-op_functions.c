/**
 * op_add - return the sum of 2 numbers
 * @a: number 1
 * @b: number 2
 * Return: sum of the two numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of two numbers
 * @a: number 1
 * @b: number 2
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of twon numbers
 * @a: number 1
 * @b: number 2
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns quotient of two numbers
 * @a: number 1
 * @b: number 2
 * Return: quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns modulus of two numbers
 * @a: number 1
 * @b: number 2
 * Return: modulos
 */

int op_mod(int a, int b)
{
	return (a % b);
}
